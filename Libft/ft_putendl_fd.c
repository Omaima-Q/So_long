#include "libft.h"

/*Writes char string "s" on file descriptor passed as an argument,
followed by a new line.*/
void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	if (s == NULL || fd < 0)
		return ;
	i = 0;
	while (s[i])
	{
		i++;
	}
	write(fd, s, i);
	write(fd, "\n", 1);
}
